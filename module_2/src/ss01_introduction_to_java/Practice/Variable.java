package Practice;

public class Variable {
    public static void main(String[] args) {
        int i = 10; // Khai bao bien i la kieu int co gia tri la 10
        float f = 20.5f; // Khai bao bien f co kieu la float co gia tri la 20.5
        double d = 20.5; // Khai bao bien d co kieu la double co gia tri la 20.5
        boolean b = true; // Khai bao bien b co kieu la bolean co gia tri la true
        char c = 'a'; // Khai bao bien c co kieu la char co gia tri la a
        String s = "Ha Noi"; // Khai bao bien s co kieu la string co gia tri la Ha Noi
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
